import { NextResponse } from 'next/server';

import { RESPONSE } from '@/enums';
import { responseError, responseSuccess } from '@/lib/utils';

export const revalidate = 600;

export async function GET() {
  // е®ҳж–№ url
  const url = 'https://top.baidu.com/api/board?platform=wise&tab=realtime';
  try {
    // иҜ·жұӮж•°жҚ®
    const response = await fetch(url);
    if (!response.ok) {
      // еҰӮжһңиҜ·жұӮеӨұиҙҘпјҢжҠӣеҮәй”ҷиҜҜпјҢдёҚиҝӣиЎҢзј“еӯ?
      throw new Error(`${RESPONSE.label(RESPONSE.ERROR)}пјҡзҷҫеә?зғӯжҗңжҰң`);
    }
    // еҫ—еҲ°иҜ·жұӮдҪ?
    const responseBody = await response.json();
    // еӨ„зҗҶж•°жҚ®
    if (responseBody.success) {
      const result: App.HotListItem[] = responseBody.data.cards[0]?.content[0]?.content.map((v) => {
        return {
          id: v.index,
          title: v.word,
          label: v.newHotName,
          url: `https://www.baidu.com/s?wd=${encodeURIComponent(v.word)}`,
          mobileUrl: v.url,
        };
      });
      return NextResponse.json(responseSuccess(result));
    }
    return NextResponse.json(responseSuccess());
  } catch {
    return NextResponse.json(responseError);
  }
}